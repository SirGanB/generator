class Antagonista {
  //Atributos
  String _nome = "";
  String _trama = "";
  String _metodos = "";
  String _fraqueza = "";

  //Construtor
  Antagonista();

  //Modificadores de acesso
  String get nome => _nome;
  set nome(String nome) => _nome = nome;

  String get trama => _trama;
  set trama(String trama) => _trama = trama;

  String get metodos => _metodos;
  set metodos(String metodos) => _metodos = metodos;

  String get fraqueza => _fraqueza;
  set fraqueza(String fraqueza) => _fraqueza = fraqueza;
}
