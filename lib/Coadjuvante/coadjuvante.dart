class Coadjuvante {
  //Atributos
  String _aparencia = "";
  String _defeitoSegredo = "";
  String _dom = "";
  String _habilidades = "";
  String _ideal = "";
  String _nome = "";
  String _maneirismo = "";
  String _tracoInteracao = "";
  String _vinculo = "";

  //Modificadores de Acesso
  String get aparencia => _aparencia;
  set aparencia(String aparencia) => _aparencia = aparencia;

  String get defeitoSegredo => _defeitoSegredo;
  set defeitoSegredo(String defeitoSegredo) => _defeitoSegredo = defeitoSegredo;

  String get dom => _dom;
  set dom(String dom) => _dom = dom;

  String get habilidades => _habilidades;
  set habilidades(String habilidades) => _habilidades = habilidades;

  String get ideal => _ideal;
  set ideal(String ideal) => _ideal = ideal;

  String get nome => _nome;
  set nome(String nome) => _nome = nome;

  String get maneirismo => _maneirismo;
  set maneirismo(String maneirismo) => _maneirismo = maneirismo;

  String get tracoInteracao => _tracoInteracao;
  set tracoInteracao(String tracoInteracao) => _tracoInteracao = tracoInteracao;

  String get vinculo => _vinculo;
  set vinculo(String vinculo) => _vinculo = vinculo;
}
